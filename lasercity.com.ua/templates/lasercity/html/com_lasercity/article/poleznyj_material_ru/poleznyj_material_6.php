                <div class="article-columns__texts">
                    <p class="article-columns__text">Суть технологии ELOS заключается в электрооптическом совмещении двух видов энергии: интенсивного импульсного света (IPL) и электромагнитных волн радиочастотного диапазона (RF-волн). Во время процедур волосяные фолликулы обрабатываются одновременно световыми и радиоволнами, которые взаимно усиливают действие друг друга. Это провоцирует гибель фолликулов и дальнейшее выпадение волосков. В косметологии элос-эпиляция считается более эффективной, чем классическая фотоэпиляция.</p>
                    <p class="article-columns__text">Лазерная эпиляция также провоцирует гибель волосков путем воздействия на их луковицы. Однако обработка фолликулов в этом случае происходит световыми волнами фиксированной длины (755, 805 или 808 нм). Считается, что применение такого вида излучения безопаснее для кожи, чем применение интенсивного импульсного света.</p>
