    <section class="article__introduce-similitude introduce-similitude" aria-labelledby="ShortPresentation">
        <h2 class="visually-hidden" id="ShortPresentation">Ввод статьи</h2>
        <p class="introduce-similitude__text">ELOS (Electro Optical Synergy) — это современный метод удаления волос на теле за счет синергического воздействия светового излучения и радиоволн. Элос-эпиляция является одним из усовершенствованных видов фотоэпиляции. Технология была разработана и запатентована израильской компанией «Syneron Medical» в 1999 году. Другие компании уже успели создать ряд похожих методик, наиболее популярной из которых является E-light. Все они имеют аналогичный принцип действия.</p>
