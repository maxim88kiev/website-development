        <div class="article-columns__title-wrapper title-lines-wrapper">
            <h2 class="article-columns__title title-lines" id="ArticleAdditionalInformationOne">Как проходит процедура</h2>
        </div>
        <div class="article-columns__wrapper">
            <div class="article-columns__container">
                <div class="article-columns__texts">
                    <p class="article-columns__text">В ходе процедуры специалист последовательно обрабатывает участки кожи манипулой лазера. Диодное излучение проникает в кожу на глубину до 1-4 мм, где поглощается меланином волосяных фолликулов и моментально трансформируется в тепловую энергию.</p>
                    <p class="article-columns__text">В результате происходит моментальное сжигание волоска, поэтому во время процедуры вы можете почувствовать характерный запах горелых волос. Сам волосяной фолликул разрушается позже, через 10-14 дней.</p>
                    <p class="article-columns__text">Лазерное излучение воздействует только на те фолликулы (10-30%), которые находятся в стадии активного роста. Чтобы избавиться от всех волосков, необходимо 8-10 процедур. После каждой обработки волос становится все меньше, затем они полностью исчезают. Чтобы добиться желаемого эффекта, важно повторять сеансы через одинаковые промежутки времени.</p>
